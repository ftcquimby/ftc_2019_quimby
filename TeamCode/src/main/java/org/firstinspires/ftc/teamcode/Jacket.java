package org.firstinspires.ftc.teamcode;

public class Jacket {
    public String size;
    public String color;
    public int zippers;

    public Jacket() {
        size = "large";
        color = "black";
        zippers = 2;
    }

    public Jacket (String size1, String color1, int zippers1) {
        size = size1;
        color = color1;
        zippers = zippers1;
    }

}
